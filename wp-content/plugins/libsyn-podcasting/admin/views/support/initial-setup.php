<h2>Initial Setup</h2>
	<p>You will need to setup an account with Libsyn if you don't have one already to host your podcast.  Please visit <a href="//www.libsyn.com">http://www.libsyn.com</a> to setup an account.</p>
	<ul>
		<li>Once you have an account your next step will be to setup a new by logging into your <a href="https://youtu.be/mMPSkd7YlRc" target="_blank"><strong><em>Libsyn Account</em></strong></a> and click on the green arrow in the upper right hand corner, then go to "Manage WordPress Plugins".</li>
		<li>Click Add New WordPress Plugin</li>
		<li>Give the plugin a name (used only for your own organizational purposes)</li>
		<li>Enter the root domain the plugin will be installed on</li>
		<li>Hit Save and write down or otherwise save the Client ID and Client Secret you are given.</li>
		<li>In your WordPress dashboard, install the Libsyn plugin from the plugin directory and make sure it is activated</li>
		<li>Click on Libsyn Podcasting in your left hand menu</li>
		<li>Enter your Client ID and Client Secret and hit Save</li>
		<li>Enter your Libsyn login credentials and hit Login</li>
		<li>Accept the permissions</li>
		<li>Under Select Show, select the show you want to use for publishing and Save your changes</li>
	</ul>
